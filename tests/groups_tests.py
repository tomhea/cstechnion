import unittest
from cstechnion.groups.L import L


class GroupTests(unittest.TestCase):
    def test_L(self):
        l = L('01', lambda b: b == '' or int(b, 2) % 3 == 0)
        assert l[5] == ['', '0', '00', '11', '000', '011', '110', '0000', '0011', '0110', '1001', '1100', '1111', '00000', '00011', '00110', '01001', '01100', '01111', '10010', '10101', '11000', '11011', '11110']

        Li, r = [], 0
        for w in (l[False]):
            Li.append(w)
            r += 1
            if r > 40:
                break
        assert Li == ['', '0', '00', '11', '000', '011', '110', '0000', '0011', '0110', '1001', '1100', '1111', '00000', '00011', '00110', '01001', '01100', '01111', '10010', '10101', '11000', '11011', '11110', '000000', '000011', '000110', '001001', '001100', '001111', '010010', '010101', '011000', '011011', '011110', '100001', '100100', '100111', '101010', '101101', '110000']


if __name__ == '__main__':
    unittest.main()
